package co.blocke.scalajack;

import co.blocke.scalajack.SJCaptureJava;

import java.math.*;

public class JavaArray {

  private BigDecimal[] bigDecs;
  private BigInteger[] bigInts;
  private java.lang.Boolean[] booleans;
  private java.lang.Byte[] bytes;
  private Character[] characters;
  private java.lang.Double[] doubles;
  private java.lang.Float[] floats;
  private java.lang.Integer[] integers;
  private java.lang.Long[] longs;
  private java.lang.Short[] shorts;
  private BigInteger[][] multi;

  public BigDecimal[] getBigDecs() { return bigDecs; }
  public void setBigDecs(BigDecimal[] n) { bigDecs = n; }

  public BigInteger[] getBigInts() { return bigInts; }
  public void setBigInts(BigInteger[] n) { bigInts = n; }

  public java.lang.Boolean[] getBooleans() { return booleans; }
  public void setBooleans(java.lang.Boolean[] n) { booleans = n; }

  public java.lang.Byte[] getBytes() { return bytes; }
  public void setBytes(java.lang.Byte[] n) { bytes = n; }

  public Character[] getCharacters() { return characters; }
  public void setCharacters(Character[] n) { characters = n; }

  public java.lang.Double[] getDoubles() { return doubles; }
  public void setDoubles(java.lang.Double[] n) { doubles = n; }

  public java.lang.Float[] getFloats() { return floats; }
  public void setFloats(java.lang.Float[] n) { floats = n; }

  public java.lang.Integer[] getIntegers() { return integers; }
  public void setIntegers(java.lang.Integer[] n) { integers = n; }

  public java.lang.Long[] getLongs() { return longs; }
  public void setLongs(java.lang.Long[] n) { longs = n; }

  public java.lang.Short[] getShorts() { return shorts; }
  public void setShorts(java.lang.Short[] n) { shorts = n; }

  public BigInteger[][] getMulti() { return multi; }
  public void setMulti(BigInteger[][] n) { multi = n; }
}